Lesson 2:Variables<br> <br> 

A variable is a name given to a memory location that stores data at runtime.<br> <br> 

The scope of a variable determines its visibility.<br><br> 
A Php global variable is accessible to all the scripts in an application.<br> <br> 

A local variable is only accessible to the script that it was defined in.<br> <br> 

Think of a variable as a glass containing water. You can add water into the glass, drink all of it, refill it again etc.<br> <br> 

The same applies for variables. Variables are used to store data and provide stored data when needed. Just like in other programming languages, PHP supports variables too. Let’s now look at the rules followed when creating variables in PHP.<br> <br> 

